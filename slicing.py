#the act of getting subsets or parts of strings, lists or tuples.

greetings = 'Hello, Moringa!'

part_one = greetings[0:5]
part_two = greetings[-8:-1]
print(part_one)
print(part_two)
part_three = greetings[0:]
print(part_three)

